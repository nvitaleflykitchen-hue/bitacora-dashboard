create or replace function bitacora_private.is_active_profile()
returns boolean
language sql
stable
security definer
set search_path = pg_catalog, bitacora
as $$
  select exists (
    select 1 from bitacora.perfiles p
    where p.id = auth.uid() and p.activo = true
  );
$$;

