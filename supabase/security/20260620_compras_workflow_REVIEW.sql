create or replace function bitacora_private.has_compras_permission(required_action text)
returns boolean
language sql
stable
security definer
set search_path = pg_catalog, bitacora
as $$
  select bitacora_private.is_admin_or_editor()
    or exists (
      select 1 from bitacora.perfil_permisos pp
      join bitacora.perfiles p on p.id = pp.perfil_id and p.activo = true
      where pp.perfil_id = auth.uid()
        and pp.modulo = 'compras'
        and pp.accion = required_action
        and pp.activo = true
    );
$$;

