create or replace function bitacora_private.sede_in_user_scope(target_sede_id integer)
returns boolean
language sql
stable
security definer
set search_path = pg_catalog, bitacora
as $$
  select exists (
    select 1
    from bitacora.perfiles p
    where p.id = auth.uid() and p.activo = true and (
      p.rol in ('admin','editor')
      or (p.rol in ('sede','encargado') and target_sede_id = any(coalesce(p.sede_ids, '{}'::integer[])))
      or (p.rol = 'grupo' and exists (
        select 1 from bitacora.sedes s
        where s.id = target_sede_id and s.grupo_id = p.grupo_id and s.activa = true
      ))
    )
  );
$$;

create or replace function bitacora_private.can_read_requerimiento(
  target_sede_id integer,
  target_solicitante_id uuid,
  target_comprador_id uuid,
  target_supervisor_id uuid,
  target_facturacion_id uuid
)
returns boolean
language sql
stable
security definer
set search_path = pg_catalog, bitacora, bitacora_private
as $$
  select bitacora_private.is_active_profile() and (
    bitacora_private.is_admin_or_editor()
    or target_solicitante_id = auth.uid()
    or bitacora_private.sede_in_user_scope(target_sede_id)
    or bitacora_private.has_compras_permission('supervise')
    or (bitacora_private.has_compras_permission('manage') and (target_comprador_id is null or target_comprador_id = auth.uid()))
    or (bitacora_private.has_compras_permission('invoice') and target_facturacion_id = auth.uid())
    or target_supervisor_id = auth.uid()
  );
$$;

