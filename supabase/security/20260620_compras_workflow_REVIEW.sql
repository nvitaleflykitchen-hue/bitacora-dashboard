create or replace function bitacora_private.protect_requerimiento_after_send()
returns trigger
language plpgsql
security definer
set search_path = pg_catalog, bitacora, bitacora_private
as $$
declare
  old_is_sent boolean := old.estado in ('Enviado','En compra','Recibido','Cumplido','Rechazado','Cancelado');
  is_buyer boolean := bitacora_private.has_compras_permission('manage')
    or bitacora_private.has_compras_permission('supervise')
    or bitacora_private.is_admin_or_editor();
  is_supervisor boolean := bitacora_private.has_compras_permission('supervise')
    or bitacora_private.is_admin_or_editor();
begin
  if new.estado is distinct from old.estado then
    if not (
      (old.estado = 'Pendiente' and new.estado in ('Observado','Aprobado','Rechazado','Cancelado'))
      or (old.estado = 'Observado' and new.estado in ('Pendiente','Rechazado','Cancelado'))
      or (old.estado = 'Aprobado' and new.estado in ('Observado','Enviado','Rechazado','Cancelado'))
      or (old.estado = 'Enviado' and new.estado in ('En compra','Recibido','Cancelado'))
      or (old.estado = 'En compra' and new.estado in ('Recibido','Cancelado'))
      or (old.estado = 'Recibido' and new.estado in ('En compra','Cumplido'))
    ) then
      raise exception 'Transicion de estado de Compras no permitida: % -> %', old.estado, new.estado;
    end if;

    if old.estado = 'Observado' and new.estado = 'Pendiente' then
      if old.solicitante_id is distinct from auth.uid() and not bitacora_private.is_purchase_approver() then
        raise exception 'Solo el solicitante o un aprobador puede reenviar un requerimiento observado';
      end if;
    elsif old.estado = 'Pendiente' and new.estado = 'Cancelado' then
      if old.solicitante_id is distinct from auth.uid() and not bitacora_private.is_purchase_approver() then
        raise exception 'Solo el solicitante o un aprobador puede cancelar el requerimiento';
      end if;
    elsif old.estado in ('Pendiente','Observado','Aprobado') then
      if not bitacora_private.is_purchase_approver() then
        raise exception 'Solo un aprobador puede realizar esta transicion';
      end if;
    elsif old.estado = 'Recibido' and new.estado = 'Cumplido' then
      if old.solicitante_id is distinct from auth.uid() and not is_buyer then
        raise exception 'Solo el solicitante o Compras puede confirmar el cumplimiento';
      end if;
    elsif not is_buyer then
      raise exception 'Solo Compras puede avanzar esta etapa';
    end if;
  end if;

  if old_is_sent and (
    new.sede_id, new.sede_nombre, new.solicitante, new.solicitante_id,
    new.cantidad, new.unidad_medida, new.descripcion, new.periodo_consumo,
    new.justificacion, new.funcion, new.sector_maquina, new.proveedor_sugerido,
    new.tipo_compra, new.urgencia, new.fecha_necesidad
  ) is distinct from (
    old.sede_id, old.sede_nombre, old.solicitante, old.solicitante_id,
    old.cantidad, old.unidad_medida, old.descripcion, old.periodo_consumo,
    old.justificacion, old.funcion, old.sector_maquina, old.proveedor_sugerido,
    old.tipo_compra, old.urgencia, old.fecha_necesidad
  ) then
    raise exception 'Los datos originales no pueden modificarse despues del envio a Compras';
  end if;

  if (
    new.comprador_id, new.supervisor_compras_id, new.facturacion_responsable_id
  ) is distinct from (
    old.comprador_id, old.supervisor_compras_id, old.facturacion_responsable_id
  ) and not is_supervisor then
    if not (
      bitacora_private.has_compras_permission('manage')
      and old.comprador_id is null
      and new.comprador_id = auth.uid()
      and new.supervisor_compras_id is not distinct from old.supervisor_compras_id
      and new.facturacion_responsable_id is not distinct from old.facturacion_responsable_id
    ) then
      raise exception 'Solo un supervisor puede asignar o reasignar responsables de Compras';
    end if;
  end if;

  if not is_buyer and (
    new.comprador_id, new.supervisor_compras_id, new.facturacion_responsable_id,
    new.proveedor_seleccionado, new.cotizacion_estado, new.orden_compra_numero,
    new.fecha_estimada_entrega, new.gestion_compras_notas
  ) is distinct from (
    old.comprador_id, old.supervisor_compras_id, old.facturacion_responsable_id,
    old.proveedor_seleccionado, old.cotizacion_estado, old.orden_compra_numero,
    old.fecha_estimada_entrega, old.gestion_compras_notas
  ) then
    raise exception 'Solo Compras puede modificar los campos de gestion';
  end if;

  return new;
end;
$$;

revoke all on function bitacora_private.protect_requerimiento_after_send() from public, anon, authenticated;
drop trigger if exists protect_requerimiento_after_send on bitacora.requerimientos;
create trigger protect_requerimiento_after_send
before update on bitacora.requerimientos
for each row execute function bitacora_private.protect_requerimiento_after_send();

